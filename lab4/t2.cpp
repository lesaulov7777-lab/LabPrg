#include <iostream>
#include <vector>
#include <random>
#include <algorithm>
#include <iomanip>
using namespace std;

int main() {
    // Настройка локали для корректного вывода
    setlocale(LC_ALL, "Russian");

    // ====================================================================
    // Пункт 1: Инициализация генератора ranlux48 и заполнение вектора
    // ====================================================================
    random_device rd;
    ranlux48 gen(rd()); // Инициализация ranlux48 случайным зерном
    uniform_int_distribution<int> distrib(0, 100); // Диапазон [0, 100]

    int n = 15; // Задаем размер n >= 10
    vector<int> arr;
    arr.reserve(n);

    // Заполнение контейнера случайными числами
    for (int i = 0; i < n; ++i) {
        arr.push_back(distrib(gen));
    }

    cout << "1. Исходный массив (n = " << n << "):" << endl;
    for (const auto& elem : arr) {
        cout << elem << " ";
    }
    cout << "\n\n";

    // ====================================================================
    // Пункт 2: Перестановка минимального и второго максимального элементов
    // ====================================================================
    // Поиск минимального элемента
    auto min_it = arr.begin();
    for (auto it = arr.begin(); it != arr.end(); ++it) {
        if (*it < *min_it) {
            min_it = it;
        }
    }

    // Поиск первого и второго максимумов
    auto max1_it = arr.begin();
    auto max2_it = arr.begin();

    // Сначала найдем абсолютный максимум
    for (auto it = arr.begin(); it != arr.end(); ++it) {
        if (*it > *max1_it) {
            max1_it = it;
        }
    }

    // Инициализируем второй максимум любым элементом, отличным от первого
    bool first_assigned = false;
    for (auto it = arr.begin(); it != arr.end(); ++it) {
        if (it != max1_it) {
            if (!first_assigned || *it > *max2_it) {
                max2_it = it;
                first_assigned = true;
            }
        }
    }

    cout << "2. Минимальный элемент: " << *min_it 
              << ", Второй максимальный: " << *max2_it << endl;

    // Обмен местами
    iter_swap(min_it, max2_it);

    cout << "   Массив после обмена мин. и 2-го макс.:" << endl;
    for (const auto& elem : arr) {
        cout << elem << " ";
    }
    cout << "\n\n";

    // ====================================================================
    // Пункт 3: Модификация массива по примеру: [35, 39, 89, 37, 96] -> [96, 35, 39, 89, 37]
    // (Циклический сдвиг вправо на 1 позицию)
    // ====================================================================
    if (!arr.empty()) {
        int last_elem = arr.back();
        // Сдвигаем элементы с конца вправо (используем итераторы)
        for (auto it = arr.end() - 1; it != arr.begin(); --it) {
            *it = *(it - 1);
        }
        arr.front() = last_elem;
    }

    cout << "3. Массив после циклического сдвига вправо на 1:" << endl;
    for (const auto& elem : arr) {
        cout << elem << " ";
    }
    cout << "\n\n";

    // ====================================================================
    // Пункт 4: Количество чётных элементов и сумма нечётных
    // ====================================================================
    int even_count = 0;
    int odd_sum = 0;

    for (const auto& elem : arr) {
        if (elem % 2 == 0) {
            even_count++;
        } else {
            odd_sum += elem;
        }
    }

    cout << "4. Результаты анализа чисел:" << endl;
    cout << "   Количество чётных элементов: " << even_count << endl;
    cout << "   Сумма нечётных элементов: " << odd_sum << "\n\n";

    // ====================================================================
    // Пункт 5: Вывод массива змейкой с правого края (строками по 10 элементов)
    // Массив длиной N * 10, первый элемент равен 10.
    // ====================================================================
    int N_rows = 5; // Например, возьмем N = 5 (всего 50 элементов)
    int total_elements = N_rows * 10;
    vector<int> snake_arr;
    snake_arr.reserve(total_elements);

    // Наполнение по условию: первый элемент равен 10, далее заполняем последовательно
    // (Или случайными, но в ТЗ указано: "Первый элемент равен 10. Необходимо выводить змейкой")
    int start_value = 10;
    for (int i = 0; i < total_elements; ++i) {
        snake_arr.push_back(start_value + i);
    }

    cout << "5. Вывод массива " << N_rows << "x10 змейкой с правого края:" << endl;
    cout << string(45, '-') << endl;

    for (int r = 0; r < N_rows; ++r) {
        // Определяем индексы начала и конца текущей строки в одномерном массиве
        int row_start = r * 10;

        if (r % 2 == 0) {
            // Четные строки (0, 2, 4...) выводятся справа налево (обратный порядок)
            // Чтобы змейка шла "с правого края"
            for (int c = 9; c >= 0; --c) {
                cout << setw(4) << snake_arr[row_start + c] << " ";
            }
        } else {
            // Нечетные строки (1, 3, 5...) выводятся слева направо (обычный порядок)
            for (int c = 0; c < 10; ++c) {
                cout << setw(4) << snake_arr[row_start + c] << " ";
            }
        }
        cout << endl;
    }
    cout << string(45, '-') << endl;

    return 0;
}